package mechabellum.server.game.api.core.participant

/** A team participating in a game. */
enum class Team {
    ATTACKER {
        override val opponent: Team
            get() = DEFENDER
    },

    DEFENDER {
        override val opponent: Team
            get() = ATTACKER
    };

    /** The team that opposes this team. */
    abstract val opponent: Team
}
